<?php

// variable 
$var = 'value';

// constant (cannot change)
define(CONSTANT, 'value');

// object's property
$object->var = 'value';

// static property of a class
my_class::$var = 'value';
// the same but from within the class
static::$var = 'value';

// a specific item within an array
$array['var'] = 'value';

// a new item within an array
$array[] = 'value';